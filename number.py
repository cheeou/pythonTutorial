# NUMBERS

# '/' Division 은 부동 소수점(float)값을 반환한다.
print(8 / 5)

# '//' Floor division (내림 나누기) 연산자는 나눗셈 결과에서 소수를 제거하고 정수만 남긴다.
# 주의점은 음수 연산일 때 내림 소수 기준에 따라 일의 자리 변동이 발생함

print(17 // 3) # 5
print(-17 // 3) # -6

# the '%' operator returns the remainder of the division
print(17 % 3) # 2

# 몫 floored quotient * 나누는 수 divisor + 나머지 remainder
print(5 * 3 + 2) # 17

# calculate powers
print(5 ** 2) # 5 squared
print(2 ** 7) # 2 to the power of 7  2의 7제곱

# 복합 연산자
# *=, /=, //=, %=, **=
a = 1
a = a + 1
a += 1 # 1만큼 증가
a -= 1 # -1만큼 감소







